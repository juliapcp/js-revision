// + - * / % **
const dois=2;
console.log(dois + 5); //7
console.log(dois - 5); //-3
console.log(dois * 5); //10
console.log(10 / dois); //5
console.log(dois ** 10); //1024
console.log(11 % dois); //1
let u = 2;
u++; //3
u += 2 //5
console.log(u); //5
console.log(u++); //5 pois ele primeiro imprime depois soma, na proxima linha vai ser 5
console.log(++u); //7, era 5 e primeiro incrementa depois imprime